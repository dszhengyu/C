ASSUME CS:CODE

CODE SEGMENT
START:
	MOV AX, CODE
	MOV DS, AX
	MOV SI, OFFSET S
	MOV AX, 0
	MOV ES, AX
	MOV DI, 200H
	MOV CX, OFFSET DONE - OFFSET S
	CLD
	REP MOVSB
	MOV AX, 0
	MOV DS, AX
	MOV AX, 200H
	MOV DS:[7CH * 4], AX
	MOV AX, 0
	MOV DS:[7CH * 4 +2], AX 
	
	MOV AX, 0B800H
	MOV ES, AX
	MOV DI, 160 * 12
	MOV BX, OFFSET K - OFFSET K1
	MOV CX, 80
K:
	MOV BYTE PTR ES:[DI], '!'
	ADD DI, 2
	INT 7CH
K1:
	NOP
	
	MOV AX, 4C00H
	INT 21H
	
S:
	DEC CX
	JCXZ OK
	ADD [SP], BX	
OK:
	IRET

DONE:
	NOP	
	
CODE ENDS
END START


